CREATE DATABASE IF NOT EXISTS ESQLDB;

USE INVESTIMENTI;

SET SQL_SAFE_UPDATES=0;

CREATE TABLE DOCENTE(
	MAIL VARCHAR(20),
	NOME VARCHAR(20),
	COGNOME VARCHAR(20),
	RECAPITO VARCHAR(20),
    DIPARTIMENTO VARCHAR(20) NOT NULL, 
    CORSO varchar(20) NOT NULL,
    
    PRIMARY KEY (MAIL, NOME, COGNOME)
)
ENGINE=INNODB;
    
CREATE TABLE STUDENTE(
	MAIL VARCHAR(20),
	NOME VARCHAR(20),
	COGNOME VARCHAR(20),
	RECAPITO VARCHAR(20),
    ANNO_IMMATRICOLAZIONE VARCHAR(20) NOT NULL, 
    CODICE CHAR(16) NOT NULL,
    
	PRIMARY KEY (MAIL, NOME, COGNOME)
)
ENGINE=INNODB;
    
    
    




